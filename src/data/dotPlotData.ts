// Generate rate ticks for y-axis (0% to 6% in 0.25% steps)
export const generateRateTicks = () => {
  const ticks = [];
  for (let i = 0; i <= 6; i += 0.25) {
    ticks.push(parseFloat(i.toFixed(2)));
  }
  return ticks;
};

export const rateTicks = generateRateTicks();
